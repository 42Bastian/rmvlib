#include "sprite.h"

void detach_sprite_from_display(sprite *s) {
  if(s->h.next != NULL) {
    s->h.next->previous = s->h.previous;
  }
  s->h.previous->next = s->h.next;
  s->h.next = NULL;
  s->h.previous = NULL;
}
